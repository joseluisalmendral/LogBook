/**
 * LogBook export — inline interactivity (ADR-24).
 *
 * Single IIFE, no external deps. All interactivity is progressive enhancement:
 * static HTML is fully readable without JS.
 *
 * Modules (all closures, executed at DOMContentLoaded — script is at end of body):
 *   loadData()          — parse #lb-data JSON blob
 *   themeToggle()       — dark/light mode button + localStorage persistence
 *   timelineFilters()   — search, event-type filter, sort, show-N for timeline table
 *   errorsFilters()     — severity/status filters for errors table
 *   dashboardRange()    — range selector toggles pre-built chart variants
 *   copyButtons()       — clipboard copy handler for .lb-copy buttons
 *   anchorLinks()       — inject <a class="lb-anchor"> into h2/h3 headings; click copies URL (IJ-6)
 *
 * Source of truth: assets/export/inline.js
 * Mirror constant:  src/export/inline-js.ts (byte-identical, generated by scripts/sync-inline-js.py)
 * Gatekeeper test:  tests/unit/inline-js-sync.test.ts
 */
(function () {
  "use strict";

  // ---------------------------------------------------------------------------
  // loadData — parse the embedded JSON data block (ADR-24)
  // ---------------------------------------------------------------------------

  /**
   * Parse the application/json element with id="lb-data".
   * Returns null on any failure — all callers must guard against null.
   *
   * @returns {object|null}
   */
  function loadData() {
    try {
      var el = document.getElementById("lb-data");
      if (!el) return null;
      return JSON.parse(el.textContent || "null");
    } catch (e) {
      return null;
    }
  }

  // ---------------------------------------------------------------------------
  // themeToggle — dark/light mode with localStorage persistence (ADR-24)
  // ---------------------------------------------------------------------------

  function themeToggle() {
    var btn = document.getElementById("lb-theme-toggle");
    if (!btn) return;

    // Determine initial theme: localStorage → OS preference → light.
    // IJ-4: key is "lb.theme" (dot separator, per spec).
    function getStoredTheme() {
      try {
        return localStorage.getItem("lb.theme");
      } catch (e) {
        return null;
      }
    }

    function setStoredTheme(theme) {
      try {
        localStorage.setItem("lb.theme", theme);
      } catch (e) {
        // Private mode or file:// origin — degrade silently.
      }
    }

    function applyTheme(theme) {
      document.documentElement.setAttribute("data-theme", theme);
      btn.textContent = theme === "dark" ? "Light mode" : "Dark mode";
      btn.setAttribute("aria-pressed", String(theme === "dark"));
    }

    var stored = getStoredTheme();
    var prefersDark =
      typeof window.matchMedia === "function" &&
      window.matchMedia("(prefers-color-scheme: dark)").matches;
    var initial = stored || (prefersDark ? "dark" : "light");
    applyTheme(initial);

    btn.addEventListener("click", function () {
      var current = document.documentElement.getAttribute("data-theme") || "light";
      var next = current === "dark" ? "light" : "dark";
      applyTheme(next);
      setStoredTheme(next);
    });
  }

  // ---------------------------------------------------------------------------
  // timelineFilters — search + type filter + sort + show-N (ADR-24, IJ-3)
  // ---------------------------------------------------------------------------

  function timelineFilters(data) {
    var searchInput = document.getElementById("lb-timeline-search");
    var typeSelect = document.getElementById("lb-timeline-type");
    var sortSelect = document.getElementById("lb-timeline-sort");
    var showSelect = document.getElementById("lb-timeline-show");
    var tbody = document.querySelector("#lb-timeline-table tbody");

    if (!tbody) return;

    var rows = Array.prototype.slice.call(tbody.querySelectorAll("tr"));
    if (rows.length === 0) return;

    var debounceTimer = null;

    function filterRows() {
      var query = searchInput ? searchInput.value.toLowerCase() : "";
      var typeFilter = typeSelect ? typeSelect.value : "";
      var sortOrder = sortSelect ? sortSelect.value : "asc";
      var showN = showSelect ? parseInt(showSelect.value, 10) : 0;

      // Sort rows by timestamp (first cell assumed to be ts).
      var sorted = rows.slice().sort(function (a, b) {
        var aTs = (a.cells[0] ? a.cells[0].textContent : "") || "";
        var bTs = (b.cells[0] ? b.cells[0].textContent : "") || "";
        return sortOrder === "desc"
          ? bTs.localeCompare(aTs)
          : aTs.localeCompare(bTs);
      });

      var visible = 0;
      sorted.forEach(function (row) {
        var text = row.textContent.toLowerCase();
        var matchQuery = !query || text.indexOf(query) !== -1;
        var matchType = !typeFilter || text.indexOf(typeFilter.toLowerCase()) !== -1;
        var show = matchQuery && matchType && (showN === 0 || visible < showN);
        row.hidden = !show;
        if (show) visible++;
      });

      // Re-append in sort order (DOM mutation).
      sorted.forEach(function (row) {
        tbody.appendChild(row);
      });
    }

    function debounced() {
      if (debounceTimer) clearTimeout(debounceTimer);
      debounceTimer = setTimeout(filterRows, 300);
    }

    if (searchInput) searchInput.addEventListener("input", debounced);
    if (typeSelect) typeSelect.addEventListener("change", filterRows);
    if (sortSelect) sortSelect.addEventListener("change", filterRows);
    if (showSelect) showSelect.addEventListener("change", filterRows);
  }

  // ---------------------------------------------------------------------------
  // errorsFilters — severity + status filter for errors table (ADR-24, IJ-4)
  // ---------------------------------------------------------------------------

  function errorsFilters(data) {
    var severitySelect = document.getElementById("lb-errors-severity");
    var statusSelect = document.getElementById("lb-errors-status");
    var tbody = document.querySelector("#lb-errors-table tbody");

    if (!tbody) return;

    var rows = Array.prototype.slice.call(tbody.querySelectorAll("tr"));
    if (rows.length === 0) return;

    function filterRows() {
      var severity = severitySelect ? severitySelect.value : "";
      var status = statusSelect ? statusSelect.value : "";

      rows.forEach(function (row) {
        var text = row.textContent.toLowerCase();
        var matchSeverity = !severity || text.indexOf(severity.toLowerCase()) !== -1;
        var matchStatus = !status || text.indexOf(status.toLowerCase()) !== -1;
        row.hidden = !(matchSeverity && matchStatus);
      });
    }

    if (severitySelect) severitySelect.addEventListener("change", filterRows);
    if (statusSelect) statusSelect.addEventListener("change", filterRows);
  }

  // ---------------------------------------------------------------------------
  // dashboardRange — toggle pre-built chart variants (ADR-24, ADR-25, IJ-6)
  // ---------------------------------------------------------------------------

  function dashboardRange() {
    var sel = document.getElementById("lb-dashboard-range");
    if (!sel) return;

    var charts = Array.prototype.slice.call(
      document.querySelectorAll(".lb-chart[data-range]")
    );

    sel.addEventListener("change", function () {
      var selected = sel.value;
      charts.forEach(function (chart) {
        var range = chart.getAttribute("data-range");
        chart.hidden = range !== selected;
      });
    });
  }

  // ---------------------------------------------------------------------------
  // copyButtons — clipboard handler for .lb-copy buttons (ADR-24, ADR-26, IJ-5)
  // ---------------------------------------------------------------------------

  function copyButtons() {
    var buttons = Array.prototype.slice.call(
      document.querySelectorAll(".lb-copy")
    );

    buttons.forEach(function (btn) {
      btn.addEventListener("click", function () {
        var wrap = btn.parentElement;
        if (!wrap) return;
        var pre = wrap.querySelector("pre");
        if (!pre) return;
        var text = pre.textContent || "";

        if (navigator.clipboard && typeof navigator.clipboard.writeText === "function") {
          navigator.clipboard.writeText(text).then(function () {
            var orig = btn.textContent;
            btn.textContent = "Copied!";
            // IJ-5: success indicator for exactly 1 second.
            setTimeout(function () {
              btn.textContent = orig;
            }, 1000);
          }).catch(function () {
            // Clipboard write failed — no-op.
          });
        }
      });
    });
  }

  // ---------------------------------------------------------------------------
  // anchorLinks — inject # anchor on h2/h3 headings and copy URL on click (IJ-6)
  // rehype-slug adds id attributes at build time; this JS injects the <a> link
  // and wires the click-to-copy handler. CSS .lb-anchor handles hover visibility.
  // ---------------------------------------------------------------------------

  function anchorLinks() {
    var headings = Array.prototype.slice.call(
      document.querySelectorAll("h2[id], h3[id]")
    );

    headings.forEach(function (heading) {
      var id = heading.getAttribute("id");
      if (!id) return;

      var anchor = document.createElement("a");
      anchor.className = "lb-anchor";
      anchor.href = "#" + id;
      anchor.setAttribute("aria-label", "Anchor link to " + heading.textContent);
      anchor.textContent = "#";

      anchor.addEventListener("click", function (event) {
        event.preventDefault();
        var url = window.location.origin + window.location.pathname + "#" + id;
        if (navigator.clipboard && typeof navigator.clipboard.writeText === "function") {
          navigator.clipboard.writeText(url).then(function () {
            var orig = anchor.textContent;
            anchor.textContent = "✓";
            setTimeout(function () {
              anchor.textContent = orig;
            }, 1000);
          }).catch(function () {
            // Clipboard unavailable — just update the URL bar.
            window.location.hash = id;
          });
        } else {
          // Fallback: update URL bar (no clipboard access).
          window.location.hash = id;
        }
      });

      heading.appendChild(anchor);
    });
  }

  // ---------------------------------------------------------------------------
  // Main — top-level try/catch so static content remains readable on JS error
  // ---------------------------------------------------------------------------

  try {
    var data = loadData();
    themeToggle();
    timelineFilters(data);
    errorsFilters(data);
    dashboardRange();
    copyButtons();
    anchorLinks();
  } catch (err) {
    if (typeof console !== "undefined") {
      console.error("[logbook]", err);
    }
  }
})();

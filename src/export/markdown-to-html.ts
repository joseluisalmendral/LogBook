// Public API
// ---------------------------------------------------------------------------

/**
 * Convert a Markdown string to an HTML body fragment.
 *
 * Used by both the HTML export pipeline (`html.ts`) and the generator
 * helpers (`sessions-doc.ts`) to render inline content.
 *
 * @param markdown       Source markdown string.
 * @param speakerBlocks  Optional speaker blocks extracted before this call.
 * @returns              HTML body fragment string.
 */
export async function markdownToHtml(
  markdown: string,
  speakerBlocks?: SpeakerBlock[],
): Promise<string> {
  // Phase 1: extract <details> blocks → tokens.
  const { markdown: withoutDetails, details } =
    preprocessDetailsPlaceholders(markdown);

  // Phase 1b: extract other raw HTML blocks → LBRAW tokens (CRITICAL-1 fix).
  // Runs AFTER details so nested <details> inside divs are already tokenized.
  const { markdown: withoutRawHtml, rawBlocks } =
    preprocessRawHtmlPlaceholders(withoutDetails);

  // Phase 2: extract mermaid fences → placeholders + SVG array.
  const { markdown: withPlaceholders, svgs } =
    await preprocessMermaidPlaceholders(withoutRawHtml);

  const processor = unified()
    .use(remarkParse)
    .use(remarkRehype)
    .use(rehypeSlug)
    .use(rehypeStringify);

  const file = await processor.process(withPlaceholders);
  let html = String(file);

  // Phase 3 (ADR-26): wrap <pre> elements before mermaid injection.
  html = wrapPreElements(html);

  // Phase 4: inject mermaid SVG divs in place of comment placeholders.
  html = injectMermaidSvgs(html, svgs);

  // Phase 5: restore <details> blocks.
  html = injectDetailsDivs(html, details);

  // Phase 5b: restore raw HTML blocks (CRITICAL-1 fix).
  html = injectRawHtmlBlocks(html, rawBlocks);

  // Phase 6 (speaker mode): inject speaker note divs.
  if (speakerBlocks && speakerBlocks.length > 0) {
    html = injectSpeakerDivs(html, speakerBlocks);
  }

  return html;
}
